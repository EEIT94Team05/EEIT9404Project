package model;

public class ReportBean {
	private String report_id;
	private String report_cause;
	private java.util.Date report_date;
	private String reporter;
	private String case_id;
	
	
	public String getReport_id() {
		return report_id;
	}
	public void setReport_id(String report_id) {
		this.report_id = report_id;
	}
	public String getReport_cause() {
		return report_cause;
	}
	public void setReport_cause(String report_cause) {
		this.report_cause = report_cause;
	}
	public java.util.Date getReport_date() {
		return report_date;
	}
	public void setReport_date(java.util.Date report_date) {
		this.report_date = report_date;
	}
	public String getReporter() {
		return reporter;
	}
	public void setReporter(String reporter) {
		this.reporter = reporter;
	}
	public String getCase_id() {
		return case_id;
	}
	public void setCase_id(String case_id) {
		this.case_id = case_id;
	}
	
	
}
